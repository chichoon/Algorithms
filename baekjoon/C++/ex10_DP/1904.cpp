#include <iostream>

int arr[1000002];

int main(void) {
	int n;

	std::cin >> n;
	arr[1] = 1;
	arr[2] = 2;

	for (int i = 3; i <= n; i++) {
		arr[i] = (arr[i - 1] + arr[i - 2]) % 15746;
	}
	std::cout << arr[n];
}

